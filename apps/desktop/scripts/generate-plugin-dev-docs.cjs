#!/usr/bin/env node
// Generate src/plugin-dev-docs.generated.ts from docs/site — the plugin-author
// documentation that the desktop app embeds into every scaffolded plugin's
// AGENTS.md (user machines don't have this repo, so the app must carry the
// docs it ships). The output module is CHECKED IN; a bun test re-runs this
// transform in-memory and fails on drift, so docs/site stays the single
// source of truth.
//
// Run after editing any of the source docs:
//   node apps/desktop/scripts/generate-plugin-dev-docs.cjs

"use strict";

const fs = require("node:fs");
const path = require("node:path");

/** Repo-relative doc files, concatenated in reading order. */
const DOC_SOURCES = [
  "docs/site/guide/plugin-anatomy.md",
  "docs/site/guide/getting-started.md",
  "docs/site/guide/lifecycle.md",
  "docs/site/guide/data-and-events.md",
  "docs/site/reference/manifest.md",
  "docs/site/reference/permissions.md",
  "docs/site/reference/backend-sdk.md",
  "docs/site/reference/frontend-sdk.md",
];

const HEADER =
  "# UnCorded plugin development reference\n\n" +
  "Everything below is the official UnCorded plugin documentation, embedded\n" +
  "verbatim for offline/agent use. Cross-page links from the docs site have\n" +
  "been flattened to plain text — every referenced page is included in this\n" +
  "file, so read on rather than following links.\n";

function repoRoot() {
  // scripts/ → apps/desktop → apps → repo root
  return path.resolve(__dirname, "..", "..", "..");
}

/**
 * Flatten VitePress-internal links: every linked page is concatenated into
 * this same document, so `[Manifest](/reference/manifest)` becomes `Manifest`.
 * External (http/https/mailto) links are kept.
 */
function flattenInternalLinks(markdown) {
  return markdown.replace(/\[([^\]]+)\]\((\/[^)]*)\)/g, "$1");
}

/** Build the single docs string from in-memory file contents. */
function buildDocsString(filesByPath) {
  const sections = DOC_SOURCES.map((rel) => {
    const raw = filesByPath[rel];
    if (typeof raw !== "string") {
      throw new Error(`missing doc source: ${rel}`);
    }
    return flattenInternalLinks(raw.trim());
  });
  return [HEADER.trim(), ...sections].join("\n\n---\n\n") + "\n";
}

/** Read the doc sources from disk (repo checkout required). */
function readDocSources(root) {
  const out = {};
  for (const rel of DOC_SOURCES) {
    out[rel] = fs.readFileSync(path.join(root, rel), "utf8");
  }
  return out;
}

/** Render the generated TypeScript module. */
function buildModuleSource(docsString) {
  return (
    "// AUTO-GENERATED by apps/desktop/scripts/generate-plugin-dev-docs.cjs — DO NOT EDIT.\n" +
    "// Source of truth: docs/site (see DOC_SOURCES in the script). A bun test\n" +
    "// (plugin-dev-docs.test.ts) fails when this file drifts from the docs.\n\n" +
    "export const PLUGIN_DEV_DOCS: string =\n" +
    "  " +
    JSON.stringify(docsString) +
    ";\n"
  );
}

function main() {
  const root = repoRoot();
  const docsString = buildDocsString(readDocSources(root));
  const outPath = path.join(root, "apps", "desktop", "src", "plugin-dev-docs.generated.ts");
  fs.writeFileSync(outPath, buildModuleSource(docsString), "utf8");
  process.stdout.write(
    `wrote ${path.relative(root, outPath)} (${docsString.length} chars from ${DOC_SOURCES.length} docs)\n`,
  );
}

module.exports = { DOC_SOURCES, buildDocsString, buildModuleSource, readDocSources, repoRoot };

if (require.main === module) {
  main();
}
